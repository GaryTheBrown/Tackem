"""special system for languages including conversion between types"""


class Language:
    """Single Language Class"""

    def __init__(self, name: str, iso_639_1: str):
        self.__name = name
        self.__iso_639_1 = iso_639_1

    @property
    def name(self):
        """Returns Name"""
        return self.__name

    @property
    def iso_639_1(self):
        """Returns 2 letter code"""
        return self.__iso_639_1


